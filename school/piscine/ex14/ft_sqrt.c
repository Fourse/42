int	ft_sqrt(int nb)
{
	int i;

	if (nb > 0)
	{
		i = 0;
		while (((i * i) <= nb) && (i < 46431))
		{
			i++;
			if ((i * i) == nb)
				return (i);
		}
	}
	return (0);
}
